<h1 class="text-5xl font-bold text-indigo-800">Location and practicalities</h1>

<p>
    Like every year, the WECEAF is hosted in Prague's amazing <b
        >Clarion Congress Hotel.</b
    >
    The hotel can be found directly at Vysočanská metro station (line B), which takes
    you to the centre of Prague in 10 minutes. You can walk to the O2 Arena in 5
    minutes. The hotel is connected to the Fénix Shopping Gallery with over 80 shops
    to meet all your wishes and requirements. If you're coming by car, you'll be
    happy to hear that you have the option of parking in an underground parking garage
    with a car wash. Just don't forget about any bikes on your roof. This is because
    the maximum permissible vehicle height is 1.8 m. For your safety, CNG and LPG
    vehicles are not permitted.
</p>

