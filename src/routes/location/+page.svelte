<h1 class="text-3xl font-bold text-indigo-800">How do I get there?</h1>
<div class="grid grid-cols-2 grid-rows-2 gap-4 [&>div]:bg-white [&>div]:p-6 [&>div]:rounded-md">
    <div>
        <h3 class="text-xl font-bold text-indigo-500">By train</h3>
        <p>
            It takes about 15 minutes by taxi to reach the hotel from the Central Train
            Station or Florenc Bus Station. We can arrange a hotel taxi. Contact the
            concierge to make a reservation (concierge.cchp@clarion⁠-⁠hotels.cz, +420
            211 131 137) or the Reservations Department
            (reservation.cchp@clarion⁠-⁠hotels.cz, +420 211 131 119). Please reserve
            transfers at least 24 hours in advance.
        </p>
    </div>
    
    <div>
        <h3 class="text-xl font-bold text-indigo-500">
            By car (from Brno / Vienna via the D1)
        </h3>
        <p>
            Take the D1 motorway towards "Centrum", turn right in the direction of
            Karlín and Vysočany, drive for roughly 5 km. Turn right into Českomoravská
            Street in the direction of the O2 Arena. Drive for roughly 1 km and turn
            left into Freyova Street. After about 600 metres you will see the Clarion
            Congress Hotel Prague**** on your left.
        </p>
    </div>

    <div>
        <h3 class="text-xl font-bold text-indigo-500">By car (from the North, E55)</h3>
        <p>
            Take the E55 and when you arrive in Prague turn right at the intersection
            with Kolbenova Street and continue towards the centre. After about 1
            kilometre you will see the Clarion Congress Hotel Prague****.
        </p>
    </div>

    <div>
        <h3 class="text-xl font-bold text-indigo-500">By plane</h3>
        <p>
            The journey from Václav Havel Airport takes 30⁠-⁠45 minutes by taxi (19.5
            km). We can arrange a hotel taxi. Contact the concierge to make a
            reservation (concierge.cchp@clarion⁠-⁠hotels.cz, +420 211 131 137) or the
            Reservations Department (reservation.cchp@clarion⁠-⁠hotels.cz, +420 211 131
            119). Please reserve transfers at least 24 hours in advance.
        </p>
    </div>
</div>
